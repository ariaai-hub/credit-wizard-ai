/**
 * Look up all statutes for a given state code.
 */
export function getStatutesForState(stateCode: string): StateLawLibraryRecord[] {
  return STATE_LAW_LIBRARY.filter(
    (r) => r.stateCode.toUpperCase() === stateCode.toUpperCase() && r.active,
  );
}

/**
 * Look up statutes by category within a state.
 */
export function getStatutesByCategory(
  stateCode: string,
  category: string,
): StateLawLibraryRecord[] {
  return getStatutesForState(stateCode).filter(
    (r) => r.claimCategory === category && r.active,
  );
}

/**
 * Returns true if a state has attorney fees shifting for consumer claims.
 */
export function stateHasAttorneyFees(stateCode: string): boolean {
  return STATE_LAW_LIBRARY.some(
    (r) =>
      r.stateCode.toUpperCase() === stateCode.toUpperCase() &&
      r.claimCategory === "consumer_protection" &&
      r.attorneyFeesFlag === true &&
      r.active,
  );
}

/**
 * Check if a particular statute applies to a given party type.
 */
export function statuteAppliesTo(
  stateCode: string,
  claimCategory: string,
  party: "CRA" | "furnisher" | "collector" | "original_creditor",
): boolean {
  return STATE_LAW_LIBRARY.some(
    (r) =>
      r.stateCode.toUpperCase() === stateCode.toUpperCase() &&
      r.claimCategory === claimCategory &&
      r.appliesTo.includes(party) &&
      r.active,
  );
}

/**
 * Get all states currently represented in the library.
 */
export function getCoveredStates(): string[] {
  return [...new Set(STATE_LAW_LIBRARY.map((r) => r.stateCode))].sort();
}

/**
 * Count total active records.
 */
export function getTotalRecordCount(): number {
  return STATE_LAW_LIBRARY.filter((r) => r.active).length;
}
